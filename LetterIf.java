public class LetterIf{
	public static void main(String[] args) {
		char text = 'e';

		if(text == 'a') {
			System.out.println(text + " este vocala");
		} else if (text == 'e') {
			System.out.println(text + " este vocala");
		} else if (text == 'y') {
			System.out.println(text + " este vocala");
		} else if (text == 'i') {
			System.out.println(text + " este vocala");
		} else if (text == 'o') {
			System.out.println(text + " este vocala");
		} else if (text == 'u') {
			System.out.println(text + " este vocala");
		} else {
			System.out.println(text + " este consoana");
		}

	}
}



